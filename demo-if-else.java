package com.mycompany.strukturkontrolpemilihan;

/**
 * Demonstrasi Struktur Kontrol Pemilihan (if-else)
 * @author luqmanaru
 */
public class StrukturKontrolPemilihan {
    public static void main(String[] args) {
        int grade = 68; // Nilai default
        
        // Struktur kontrol if-else
        if (grade > 60) {
            System.out.println("YEY NGGAA NGULANG!");
        } else {
            System.out.println("YAH NGULANG LAGI BRO 1");
        }
    }
}
